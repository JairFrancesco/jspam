/**
 * @author PNT
 *
 */
public class Mail {
    private String asunto;
    private String body;

    /**
     * @return the asunto.
     */
    public String getAsunto() {
        return asunto;
    }

    /**
     * @param asunto
     *            the asunto to set.
     */
    public void setAsunto(String asunto) {
        this.asunto = asunto;
    }

    /**
     * @return the body.
     */
    public String getBody() {
        return body;
    }

    /**
     * @param body
     *            the body to set.
     */
    public void setBody(String body) {
        this.body = body;
    }
    
    
}
